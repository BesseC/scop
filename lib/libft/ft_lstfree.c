#include "libft.h"

void	ft_lstfree(t_list **begin_list)
{
	t_list	*t;
	t_list	*f;

	t = *begin_list;
	while (t)
	{
		f = t;
		t = t->next;
		free(f);
	}
	*begin_list = t;
}
