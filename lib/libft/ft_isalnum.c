#include "libft.h"

int	ft_isalnum(int c)
{
	if ((c >= 0x41 && c <= 0x5A) || (c >= 0x61 && c <= 0x7A)
			|| (c >= 0x30 && c <= 0x39))
		return (1);
	else
		return (0);
}
