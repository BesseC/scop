#include "libft.h"

void	*ft_memccpy(void *dest, const void *src, int c, size_t n)
{
	unsigned char	*d;
	unsigned char	*s;
	unsigned char	j;
	size_t			i;

	j = (unsigned char)c;
	s = (unsigned char *)src;
	d = (unsigned char *)dest;
	i = 0;
	while (i < n)
	{
		d[i] = s[i];
		if (s[i] == j)
		{
			return (dest + (i + 1));
		}
		i++;
	}
	return (NULL);
}
