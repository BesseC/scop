#include "includes/libmat.h"

t_mat4  mat4_sub(t_mat4 m1, t_mat4 m2)
{
  t_mat4 m3;
  int i;

  i = -1;
  while(++i < 16)
    m3.m[i] = m1.m[i] - m2.m[i];
  return(m3);
}
