#include "includes/libmat.h"

t_vec3	vec3_cross(t_vec3 v1, t_vec3 v2)
{
	t_vec3	v3;

	v3.x = ((v1.y * v2.z) - (v1.z * v2.y));
	v3.y = ((v1.z * v2.x) - (v1.x * v2.z));
	v3.z = ((v1.x * v2.y) - (v1.y * v2.x));
	return (v3);
}
