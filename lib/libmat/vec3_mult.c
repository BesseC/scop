#include "includes/libmat.h"

t_vec3	vec3_mult(t_vec3 v1, double i)
{
	t_vec3	v2;

	v2.x = v1.x * i;
	v2.y = v1.y * i;
	v2.z = v1.z * i;
	return (v2);
}
