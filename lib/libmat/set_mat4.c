#include "includes/libmat.h"

void	set_mat4(t_mat4 *m, float f)
{
	int	i;

	i = -1;
	while (++i < 16)
	{
		m->m[i] = f;
	}
}

void	mat4_id(t_mat4 *m)
{
	int	i;

	i = -1;
	while (++i < 16)
	{
		if (i == 0 || i == 5 || i == 10 || i == 15)
			m->m[i] = 1;
		else
			m->m[i] = 0;
	}
}
